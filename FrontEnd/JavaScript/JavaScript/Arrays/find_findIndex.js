// Methods that iterate over an array
// Methods that DOT NOt modify the array original array (Inmutability)

/* find() */
const multiple0f5 = [5, 10, 15, 20]

/* Numeros mayores de 10 */
const firstNumberGreaterThan10 = multiple0f5.find(number => number > 10)
console.log(multiple0f5);
console.log(firstNumberGreaterThan10);


// findIndex()
const randomNumbers = [6, 14, 27, 56, 40]
/* Trae el  */
const indexNumber = randomNumbers.findIndex(number => number > 50)
console.log(randomNumbers);
console.log(indexNumber);



