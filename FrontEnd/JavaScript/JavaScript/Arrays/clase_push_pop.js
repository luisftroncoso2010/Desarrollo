// Methods that modify the original array (Mutability)

// Push()
const conuntries = ['USA', 'Canada', 'UK']
const newCountries = conuntries.push('Germany', 'Australia')

console.log(conuntries);
console.log(newCountries);


// Pop()
const removeCountry = conuntries.pop()
console.log(conuntries);
console.log(removeCountry);  // Muestra el elemento removido



