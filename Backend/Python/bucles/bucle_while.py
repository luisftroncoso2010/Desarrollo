'''
El buble while solo se ejecuta si la condicion es REAL, es decir True 
'''
contador = 1

print('---Imprimir los numeros del 0 al 10---')
while contador < 10:
    print(contador)
    contador +=1

print('---Imprimir los numero del 2 al 10, de dos en dos---')
contador2 = 2
while contador2 <= 10:    
    print(contador2)
    contador2 +=2
'''
Mientras que la condicion se cumple el buble se va a ejecutar 
(Vuelta, tras vuelta se verifica la condicion)
''' 
print('-----')
contador3 = 0
while contador3 < 10:    
    contador3 +=2
    print(contador3)
    