'''Funciones matematicas integradas '''
#creamos una tupla
grupoUno = (5, 10, 20, 3)
grupoDos = (3, 10, 1, 4)
#Imprimimos el numero menor de la tupla, es decir de un iterable
print(f'El valor minimo del iterable: {min(grupoUno)}')
print(f'El valor alto del iterable: {max(grupoDos)}')

#Valor abosoluto positivo
valor_absoluto = abs(-10.5)
print(f'Valor absoluto positivo: {valor_absoluto}')

#Imprimir el valor con exponentes
exponente = pow(4, 3)#El primera valor es el numero y el segundo la elevacion
print(f'El valor con exponente: {exponente}')



